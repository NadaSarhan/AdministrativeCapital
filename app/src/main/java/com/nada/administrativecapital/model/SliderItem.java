package com.nada.administrativecapital.model;

public class SliderItem {

    private int image;
    private String descript;

    public SliderItem(int image, String descript) {
        this.image = image;
        this.descript = descript;
    }

    public int getImage() {
        return image;
    }

    public String getDescript() {
        return descript;
    }

}
